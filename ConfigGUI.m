% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
[FileName,PathName] = uigetfile('*.sif','Select the SIF File');
% If no sif file selected, return
if ~FileName
    return
end
SIFFile = strcat(PathName,FileName);
rc = atsif_readfromfile(SIFFile);
if (rc ~= 22002)
    error('Could not load file.\n Return Code: %d',rc)
end
Signal = 0;
[~,present]=atsif_isdatasourcepresent(Signal);
if ~present
    error('No signal data in sif file!')
end
[~,ExpTime] = atsif_getpropertyvalue(Signal,'ExposureTime');
[~,HeadModel] = atsif_getpropertyvalue(Signal,'HeadModel');
set(handles.edit1,'String',ExpTime);
if strcmp(HeadModel,'DV897_BV')
    set(handles.edit2,'String','160');
elseif strcmp(HeadModel,'DV887')
    set(handles.edit2,'String','80');
else
    set(handles.edit2,'String','Camera type not supported!');
end
clc


